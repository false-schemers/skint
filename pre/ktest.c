static void print_block(const char *name, int *pb)
{
  char buf[1000];
  if (!pb) sprintf(buf, "%s = (null)", name);
  else sprintf(buf, "%s: chars=%d, bytes=%d, utf-8=\"%s\"\n",
    name, pb[0], pb[1], string_to_cstr(pb));
  fputs(buf, stderr);
}

static void ok(const char *msg, int nt, int np) 
{
  if (ntests-nt == npassed-np) {
    fprintf(stderr, "OK: all %s tests passed\n", msg);
  } else { 
    fprintf(stderr, "*** FAIL: %d %s tests failed\n", (ntests-nt)-(npassed-np), msg);
  }
}

static void string_test_e(int ok, const char *expr)
{
  if (ok) { npassed += 1;
    fprintf(stderr, "OK: %s\n", expr);
  } else { 
    fprintf(stderr, "*** FAIL: %s\n", expr);
  }
  ntests += 1;
}
#define string_test(e) string_test_e(e, #e)

