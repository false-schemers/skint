/* A "string block" here is heap-allocated block casted to int pointer, int *pb. 
This block contains two ints at pb[0] and pb[1], the rest of the block, starting
from &pb[2] is a zero-terminated utf-8 string. 
The first int, pb[0], contains the number of characters (code points) in the string.
The second int, pb[1], contains the number of bytes in the string, excluding the terminating zero.
Invariant: if pb[0] == pb[1], the string only contains ascii characters. */

/* encode one code-point into buf[0..3], return byte length 1..4 */
static int utf8_encode(unsigned char *buf, int c)
{
  if (c < 0x80) { buf[0] = c; return 1; }
  if (c < 0x800) {
    buf[0] = 0xC0 | (c >> 6);
    buf[1] = 0x80 | (c & 0x3F);
    return 2;
  }
  if (c < 0x10000) {
    buf[0] = 0xE0 | (c >> 12);
    buf[1] = 0x80 | ((c >> 6) & 0x3F);
    buf[2] = 0x80 | (c & 0x3F);
    return 3;
  }
  buf[0] = 0xF0 | (c >> 18);
  buf[1] = 0x80 | ((c >> 12) & 0x3F);
  buf[2] = 0x80 | ((c >> 6) & 0x3F);
  buf[3] = 0x80 | (c & 0x3F);
  return 4;
}

/* decode one code-point starting at s, advance *sp, return code-point or -1 on error */
static int utf8_decode(const unsigned char **sp)
{
  const unsigned char *s = *sp;
  if (s[0] < 0x80) {                 /* 1 byte */
    *sp = s + 1;
    return s[0];
  } else if ((s[0] & 0xE0) == 0xC0) { /* 2 byte */
    int c = ((s[0] & 0x1F) << 6) | (s[1] & 0x3F);
    *sp = s + 2;
    return c;
  } else if ((s[0] & 0xF0) == 0xE0) { /* 3 byte */
    int c = ((s[0] & 0x0F) << 12) | ((s[1] & 0x3F) << 6) | (s[2] & 0x3F);
    *sp = s + 3;
    return c;
  } else if ((s[0] & 0xF8) == 0xF0) { /* 4 byte */
    int c = ((s[0] & 0x07) << 18) | ((s[1] & 0x3F) << 12)
      | ((s[2] & 0x3F) << 6)  | (s[3] & 0x3F);
    *sp = s + 4;
    return c;
  }
  return -1; /* invalid lead byte */
}

/* Advance <s> by <n> valid UTF-8 sequences.  n==0 is no-op. */
static const unsigned char *utf8_skip_n(const unsigned char *s, int n)
{
  while (n-- > 0) {
    unsigned char b = *s;
    if (b < 0x80) { ++s; continue; }
    do { ++s; } while ((*s & 0xC0) == 0x80);
  }
  return s;
}

/* allocate a new string block, containing utf-8 encoding of char c, repeated n times */
int *new_string(int n, int c)
{
  unsigned char buf[4], *s, *end;
  int len = (c < 0x80) ? (buf[0] = c, 1) : utf8_encode(buf, c);
  int bc = n * len, *pb = malloc(sizeof(int)*2 + bc + 1); assert(pb);
  pb[0] = n, pb[1] = bc;
  s = (unsigned char *)(pb + 2), end = s + bc;
  if (len == 1) memset(s, buf[0], bc);
  else for (; s < end; s += len) memcpy(s, buf, len);
  *end = 0;
  return pb;
}

/* retrieve code point at index i, decoding utf-8 */
int string_get_char(int *pb, int i)
{
  const unsigned char *s, *si;
  assert(pb && i >= 0 && i < pb[0]);
  s = (const unsigned char*)(pb + 2);
  if (pb[0] == pb[1]) return s[i];
  si = utf8_skip_n(s, i);
  return utf8_decode(&si);
}

/* replace code point at index i to c, return (possibly realloc'd) pb */
int *string_set_char(int *pb, int i, int c)
{
  unsigned char buf[4], *s, *si, *e, *sip1;
  int oldlen, len, oldbc, bc, ioff, ipoff, eoff;
  assert(pb && i >= 0 && i < pb[0]);
  s = (unsigned char*)(pb + 2);
  if (pb[0] == pb[1] && c < 0x80) { s[i] = c; return pb; }
  si = (unsigned char*)utf8_skip_n(s, i);
  sip1 = (unsigned char*)utf8_skip_n(si, 1);
  oldlen = (int)(sip1 - si), len = utf8_encode(buf, c);
  if (len == oldlen) { memcpy(si, buf, len); return pb; }
  oldbc  = pb[1]; bc = oldbc + len - oldlen; e = s + oldbc;
  if (bc < oldbc) memmove(si+len, sip1, e-sip1+1);
  ioff = (int)(si - s); ipoff = (int)(sip1 - s); eoff = (int)(e - s);
  pb = realloc(pb, sizeof(int)*2 + bc + 1); assert(pb);
  s = (unsigned char*)(pb + 2); 
  si = s + ioff; sip1 = s + ipoff; e = s + eoff;
  if (bc > oldbc) memmove(si+len, sip1, e-sip1+1); 
  memcpy(si, buf, len);
  pb[1] = bc;
  return pb;
}

/* append pbc string blocks */
int *string_append(int pbc, int *pbv[])
{
  int i, n = 0, bc = 0, *pb; unsigned char *s;
  assert(pbc >= 0);
  for (i = 0; i < pbc; ++i) { assert(pbv[i]); n += pbv[i][0]; bc += pbv[i][1]; }
  pb = malloc(sizeof(int)*2 + bc + 1); assert(pb);
  pb[0] = n; pb[1] = bc; s = (unsigned char*)(pb + 2);
  for (i = 0; i < pbc; ++i) {
    int *pbi = pbv[i], bci = pbi[1];
    memcpy(s, (const char*)(pbi + 2), bci); s += bci;
  }
  *s = 0;
  return pb;
}

/* make a string block from a zero-terminated utf-8 string */
int *string_from_cstr(const char *cstr)
{
  int n = 0, bc = 0, *pb; const unsigned char *s;
  assert(cstr); s = (const unsigned char *)cstr;
  while (*s) {
    const unsigned char *s0 = s;
    int c = utf8_decode(&s);
    if (c < 0) return NULL; /* invalid UTF-8 */
    ++n; bc += (int)(s - s0);
  }
  pb = malloc(sizeof(int)*2 + bc + 1); assert(pb);
  pb[0] = n; pb[1] = bc;
  memcpy(pb + 2, cstr, bc + 1);
  return pb;
}

const char *string_to_cstr(const int *pb)
{
  assert(pb);
  return (const char *)(pb + 2);
}

