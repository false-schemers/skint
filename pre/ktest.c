{
  setutf8cp();
  if (utf8) puts("UTF-8 locale is set");
  else puts("Failed to set UTF-8 locale!");  

  prop_tests();
  case_tests();
  string_tests();

  fprintf(stderr, "\nTotal %d out of %d tests passed.\n", npassed, ntests);
  return 0;
}