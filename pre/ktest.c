{
  int nt = ntests, np = npassed;
  int *a = new_string(5, 'A');          /* ASCII */
  int *b = new_string(3, 0x03B1);       /* Greek alpha, 2 bytes */

  print_block("a", a);
  print_block("b", b);
  string_test(a && a[0] == 5 && a[1] == 5);
  string_test(strcmp((char*)(a + 2), "AAAAA") == 0);

  string_test(b && b[0] == 3 && b[1] == 6);  /* 3 × 2 bytes */
  string_test(string_get_char(b, 1) == 0x03B1);

  free(a); free(b);

  ok("new_string", nt, np);
}

static void test_get_char(void)
{
  int nt = ntests, np = npassed;
  int *pb = new_string(4, 0x1F600);     /* emoji, 4 bytes each */
  string_test(pb[0] == 4 && pb[1] == 16);
  string_test(string_get_char(pb, 0) == 0x1F600);
  string_test(string_get_char(pb, 3) == 0x1F600);
  print_block("pb", pb);
  free(pb);

  ok("string_get_char", nt, np);
}

static void test_set_char(void)
{
  int nt = ntests, np = npassed;
  int *pb = new_string(3, 'x');         /* "xxx" */
  print_block("pb", pb);
  pb = string_set_char(pb, 1, 0x00E9);  /* é, 2 bytes */
  string_test(pb[0] == 3);
  string_test(pb[1] == 4);                   /* 2+1+1 */
  string_test(string_get_char(pb, 1) == 0x00E9);
  pb = string_set_char(pb, 1, 'y');     /* shrink back to ASCII */
  string_test(pb[1] == 3);
  string_test(string_get_char(pb, 1) == 'y');
  string_test(0 == strcmp(string_to_cstr(pb), "xyx"));
  print_block("pb", pb);
  free(pb);
  pb = string_from_cstr("рено в xxxx, пежо в yyyy");
  print_block("pb", pb);
  pb = string_set_char(pb, 9, string_get_char(pb, 0));
  pb = string_set_char(pb, 10, string_get_char(pb, 1));
  pb = string_set_char(pb, 7, string_get_char(pb, 2));
  pb = string_set_char(pb, 8, string_get_char(pb, 3));
  pb = string_set_char(pb, 22, string_get_char(pb, 13));
  pb = string_set_char(pb, 23, string_get_char(pb, 14));
  pb = string_set_char(pb, 20, string_get_char(pb, 15));
  pb = string_set_char(pb, 21, string_get_char(pb, 16));
  print_block("pb", pb);
  pb = string_set_char(pb, 7, '0');
  pb = string_set_char(pb, 8, '1');
  pb = string_set_char(pb, 9, '2');
  pb = string_set_char(pb, 10, '3');
  pb = string_set_char(pb, 20, '4');
  pb = string_set_char(pb, 21, '5');
  pb = string_set_char(pb, 22, '6');
  pb = string_set_char(pb, 23, '7');
  string_test(0 == strcmp(string_to_cstr(pb), "рено в 0123, пежо в 4567"));
  print_block("pb", pb);
  free(pb);

  ok("string_set_char", nt, np);
}

static void test_append(void)
{
  int nt = ntests, np = npassed;
  int *v[3], *cat;
  v[0] = new_string(2, 'A');
  v[1] = new_string(2, 0x03B1);         /* αα */
  v[2] = new_string(1, '!');
  print_block("v[0]", v[0]);
  print_block("v[1]", v[1]);
  print_block("v[2]", v[2]);

  cat = string_append(3, v);
  string_test(cat != NULL);
  string_test(cat[0] == 5);                  /* 2+2+1 chars */
  string_test(cat[1] == 2+4+1);              /* 2+4+1 bytes */
  string_test(strcmp((char*)(cat + 2), "AAαα!") == 0);
  print_block("cat", cat);
  free(cat);

  cat = string_append(0, NULL);
  string_test(cat != NULL);
  string_test(cat[0] == 0);
  string_test(cat[1] == 0);
  string_test(0 == strcmp(string_to_cstr(cat), ""));
  print_block("cat", cat);
  free(cat);

  cat = string_append(1, v);
  string_test(0 == strcmp(string_to_cstr(cat), "AA"));
  print_block("cat", cat);
  free(cat);

  for (int i = 0; i < 3; ++i) free(v[i]);
  ok("string_append", nt, np);
}

static void test_from_cstr(void)
{
  int nt = ntests, np = npassed;
  int *pb;

  /* valid UTF-8 */
  pb = string_from_cstr("Hello");
  string_test(pb && pb[0] == 5 && pb[1] == 5);
  print_block("pb", pb);
  free(pb);

  pb = string_from_cstr("αβγ");
  string_test(pb && pb[0] == 3 && pb[1] == 6);
  print_block("pb", pb);
  free(pb);

  /* invalid sequence */
  unsigned char bad[] = { 0xFF, 0xFE, 0x00 };
  pb = string_from_cstr((char*)bad);
  string_test(pb == NULL);

  ok("string_from_cstr", nt, np);
}

static void string_tests(void)
{
  test_new_string();
  test_get_char();
  test_set_char();
  test_append();
  test_from_cstr();
}

