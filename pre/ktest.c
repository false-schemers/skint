/* returns 'l'=lowercase, 'u'=uppercase, 's'=whitespace, 'a'=alpha, 0-9=digit, -1 on error */
int uprop(int c) {
  uint32_t uc = (uint32_t)c, k; int lo, hi;
  if (uc > 0x10FFFF) return -1; k = uc << 8;
  lo = 0, hi = sizeof(uprops)/sizeof(uint32_t)-1;
  while (lo <= hi) {
    int mid = (lo + hi) >> 1;
    uint32_t mv = uprops[mid]; int mvc; 
    if (k < (mv & 0xFFFFFF00)) hi = mid-1; 
    else if (k >= (uprops[mid+1] & 0xFFFFFF00)) lo = mid+1; 
    else switch (mvc = (int)(mv & 0x000000FF)) {
    case '0': case '1': case '2': case '3': case '4':
    case '5': case '6': case '7': case '8': case '9':
      return mvc-'0';
    case 'd': return (int)(uc - (mv >> 8));
    case 'l': case 'u': case 'a': case 's': case '?': 
      return mvc;
    case 'L': return (uc - (mv >> 8)) % 2 ? 'u' : 'l';
    case 'U': return (uc - (mv >> 8)) % 2 ? 'l' : 'u';
    default: assert(0);
    }
  }
  assert(0); return -1;
}

