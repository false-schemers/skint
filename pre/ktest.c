/* returns new char (code point), or -1 on error */
static int ucase_to(int c, const uint64_t *table, int tsize) {
  uint64_t uc = (uint64_t)c, k; int lo, hi;
  if (uc > 0x10FFFF) return -1; k = uc << 32;
  lo = 0, hi = tsize-1;
  while (lo <= hi) {
    int mid = (lo + hi) >> 1;
    uint64_t mv = table[mid]; int32_t mvc; 
    if (k < (mv & 0xFFFFFFFF00000000ULL)) hi = mid-1; 
    else if (k >= (table[mid+1] & 0xFFFFFFFF00000000ULL)) lo = mid+1; 
    else switch (mvc = (int32_t)(uint32_t)(mv & 0x00000000FFFFFFFFULL)) {
      case CASEIDENTITY: return -42; /* no fall-thru! */
      case CASEPLUS_01:  return (uc - (mv >> 32)) % 2 ? c+1 : c;
      case CASEPLUS_10:  return (uc - (mv >> 32)) % 2 ? c : c+1;
      case CASEMINUS_01: return (uc - (mv >> 32)) % 2 ? c-1 : c;
      case CASEMINUS_10: return (uc - (mv >> 32)) % 2 ? c : c-1;
      default: return c+(int)mvc;
    }
  }
  assert(0); return -1;
}

/* return new char (code point), or -1 on error */

int utolower(int c) {
  return ucase_to(c, utolc, sizeof(utolc)/sizeof(uint64_t));
}

int utoupper(int c) {
  return ucase_to(c, utouc, sizeof(utouc)/sizeof(uint64_t));
}

int utotitle(int c) {
  int tc = ucase_to(c, utotc, sizeof(utotc)/sizeof(uint64_t));
  if (tc == -42) return c; /* special no-fall-back-to-uc identity case */
  return (tc != c) ? tc : ucase_to(c, utouc, sizeof(utouc)/sizeof(uint64_t)); 
}

int utofold(int c) {
  int fc = ucase_to(c, ufold, sizeof(ufold)/sizeof(uint64_t));
  if (fc == -42) return c; /* special no-fall-back-to-lc identity case */
  return (fc != c) ? fc : ucase_to(c, utolc, sizeof(utolc)/sizeof(uint64_t));
}



