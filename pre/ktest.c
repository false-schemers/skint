static void case_test(int c, int to, int expect)
{
  int got;
  switch (to) {
    case 'l': got = utolower(c); break; 
    case 'u': got = utoupper(c); break; 
    case 't': got = utotitle(c); break; 
    case 'f': got = utofold(c); break; 
    default: assert(0);
  }
  if (got == expect) { ++npassed; fprintf(stderr, "OK: uto%c(U+%04X) -> U+%04X\n", to, c, got); }
  else fprintf(stderr, "*** FAIL: uto%c(U+%04X) -> U+%04X (expected U+%04X)\n", to, c, got, expect);
  ++ntests;
}

