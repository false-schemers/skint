/* local function that checks if c can be a part of a number and/or a clean symbol *
 * returns 1 for initial, 2 for subsequent, 0 for none of the above */
int isnumsym(int c) {
  if (0 <= c && c < 0x80) return inisub_map[c];
  switch (uprop(c)) {
    case 'l': case 'u': case 'a': case 'i': return 1;
    case 0: case 1: case 2: case 3: case 4: case 5: case 6: case 7: case 8: case 9:
    case 's': return 2;
  }
  return 0;
}

/* local procedure that checks if s[0..blen-1] is a proper syntax of a 'clean' symbol */
int cleansymname(const char *s, int blen) {
  const char *p = s, *e = s + blen; int t0 = -1;
  if (blen < 1) return 0;
  while (p < e) {
    int t = isnumsym(udecode(&p));
    if (!t) return false; else if (t0 < 0) t0 = t;
  }
  if (t0 == 1) return 1;
  if (s[0] == '+' || s[0] == '-') {
    if (blen == 1) return 1;
    if (blen == 2 && (s[1] == 'i' || s[1] == 'I')) return 0;
    if (blen == 6 && (strcmp_ci(s+1, "inf.0") == 0 || strcmp_ci(s+1, "nan.0") == 0)) return 0;
    if (blen >= 2 && s[1] == '.' && !isdigit(s[2])) return 1; 
    if (s[1] != '.' && !isdigit(s[1])) return 1;
    return 0;
  }
  return (blen >= 2 && s[0] == '.' && !isdigit(s[1])); 
}


